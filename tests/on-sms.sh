#!/bin/bash

# Задаем путь к целевой директории
TARGET_DIR="/tmp/sms-daemon/test-out"

# Создаем директорию, если она не существует
mkdir -p "$TARGET_DIR"

# Формируем имя файла на основе текущего времени
FILENAME="${TARGET_DIR}/$(date +%Y-%m-%d_%H-%M-%S).txt"

# Записываем все переданные аргументы в файл
echo "$*" > "$FILENAME"

# Выводим сообщение о результате
echo "Параметры успешно сохранены в файл: $FILENAME"
